package ru.example.shelter.controller;

import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Controller;
import org.springframework.ui.Model;
import org.springframework.web.bind.annotation.*;
import ru.example.shelter.model.Animal;
import ru.example.shelter.service.AnimalService;

@Controller //?????????, ??? ????? ???????? ????????????
@RequestMapping("/animal") //??? ???? ??????? ??????????? ????? ?????????? ? /animal
public class AnimalController {

    @Autowired
    private AnimalService animalService;

    @GetMapping
    public String list(@RequestParam(required = false) String type, Model model) { //?????????? ????????????? ?? ??????? ????????
        model.addAttribute("animals", animalService.getAllNoTakenByType(type));
        return "animal/list";
    }

    @GetMapping("/taken/{id}")
    public String markAsTaken(@PathVariable Long id) { //???????? ???????? ??? ??????, ????? ?????????? ?? ???????? ????? ?????????
        animalService.taken(id);
        return "redirect:/animal/" + id;
    }

    @GetMapping("/new")
    public String newAnimal(Model model) { //?????????? ????????????? ? ?????? ??? ?????????? ?????????
        model.addAttribute("animal", new Animal());
        return "animal/new";
    }

    @PostMapping("/new")
    public String saveAnimal(@ModelAttribute("animal") Animal animal) { //??????????????? ????????? ???????? ? ???? ??????
        animalService.save(animal);
        return "redirect:/animal";
    }

    @GetMapping("/{id}")
    public String one(@PathVariable("id") Long id, Model model) { //?????????? ????????????? ? ??????????? ? ?????????? ????????
        model.addAttribute("animal", animalService.getById(id));
        return "animal/one";
    }

    @GetMapping("/delete/{id}")
    public String delete(@PathVariable("id") Long id) { //??????? ????????
        animalService.delete(id);
        return "redirect:/animal";
    }

    @GetMapping("/category")
    public String categories() {
        return "animal/category";
    }
    
}
