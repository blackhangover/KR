package ru.example.shelter.service;

import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.security.core.userdetails.UserDetails;
import org.springframework.security.core.userdetails.UserDetailsService;
import org.springframework.security.core.userdetails.UsernameNotFoundException;
import org.springframework.security.crypto.password.PasswordEncoder;
import org.springframework.stereotype.Service;
import ru.example.shelter.model.User;
import ru.example.shelter.repo.UserRepo;

@Service
public class UserService implements UserDetailsService {

    @Autowired
    private UserRepo userRepo;
    @Autowired
    private PasswordEncoder passwordEncoder;

    public void save(User user) { //????????? ?????? ???????????? ? ??
        user.setPassword(passwordEncoder.encode(user.getPassword())); //???????? ?????? ????????????
        user.setRole("USER"); //???????????? ???? USER
        userRepo.save(user);
    }

    public User getById(Long id) {
        return userRepo.findById(id).orElse(new User());
    }

    @Override
    public UserDetails loadUserByUsername(String username) throws UsernameNotFoundException { //?????, ??????? ???? ? ??????????? UserDetailsService, ????? ??? ????, ??? ?? ?????? ??? ?????? ????????????? ? ??
        return userRepo.findByUsername(username);
    }
}
